import { ApiProperty } from '@nestjs/swagger';

export class CallbackRequestDto {
  @ApiProperty()
  id: string;
  @ApiProperty()
  isResolved: boolean;
  @ApiProperty({
    type: 'string',
    format: 'date-time',
  })
  createdAt: Date;
  @ApiProperty({
    type: 'string',
    format: 'date-time',
  })
  updatedAt: Date;
  @ApiProperty({
    type: 'string',
    format: 'date-time',
    nullable: true,
  })
  deletedAt: Date | null;
}
